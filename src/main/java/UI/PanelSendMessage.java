    private void btnEnviarMensajeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarMensajeActionPerformed
        Mensaje mensaje = new Mensaje(contenidoMensaje.getText(), pub.getId(), pEnvia.getCi(), pRecibe.getCi(), false, LocalDateTime.now());
        boolean isConnectedWithDB = this.db.connectToDB();
        if (isConnectedWithDB) {
            try {
                this.db.insertNewMessage(mensaje);
                this.db.closeConnectionDB();

                PanelSucces panel = new PanelSucces(this.frame, "Mensaje enviado con Ã©xito.");
                JDialog dialog = new JDialog(this.frame, true);
                dialog.setSize(panel.getPreferredSize());
                dialog.setLocationRelativeTo(null);
                dialog.getContentPane().add(panel);
                dialog.setVisible(true);

            } catch (SQLException ex) {
                Logger.getLogger(PanelSendMessage.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_btnEnviarMensajeActionPerformed

